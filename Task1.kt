fun main() {                                 // Точка входа в программу
    val n = 123L                             // Задаём число напрямую (заменяет readLine)
    
    var temp = n                             // Копируем число для обработки
    var sum = 0L                             // Переменная для хранения суммы цифр
    var product = 1L                         // Переменная для хранения произведения цифр

    while (temp > 0) {                       // Пока число больше нуля
        val digit = (temp % 10).toInt()      // Берём последнюю цифру числа
        sum += digit                         // Добавляем цифру к сумме
        product *= digit                     // Умножаем произведение на цифру
        temp /= 10                           // Убираем последнюю цифру
    }

    println("Число = $n")                    // Выводим исходное число
    println("Сумма цифр = $sum, Произведение цифр = $product") // Выводим результат
}

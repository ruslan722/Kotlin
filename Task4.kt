fun main() {
    val n = 6                                 // Количество чисел в последовательности

    val seq = mutableListOf(1L, 2L, 3L)      // Первые три числа задаём вручную
    if (n <= 3) {                             // Если нужно только первые три числа
        println(seq.take(n).joinToString(", ")) // Выводим только первые n элементов
        return
    }

    for (i in 3 until n) {                    // Для остальных чисел
        val next = seq[i - 1] + seq[i - 2] + seq[i - 3] // Каждое число = сумма трёх предыдущих
        seq.add(next)                          // Добавляем новое число в список
    }

    println("Первые $n чисел последовательности: ${seq.joinToString(", ")}") // Выводим результат
}
